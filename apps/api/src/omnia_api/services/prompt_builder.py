def _compute_skill_brief(
    user_prompt: str | None, project_id: str | None
) -> str | None:
    """Pull a project-specific design brief out of the vendored `ui-ux-pro-max`
    library (`apps/api/skills/ui-ux-pro-max/`).

    Strategy: extract bag-of-words from the user prompt (with RU→EN industry
    expansion via `_expand_ru_to_en`), match against 6 skill CSVs — colors,
    typography, landing patterns, style presets, icon families, chart types
    — plus pull 5 high-severity UX guidelines. Returns a compact
    `format_design_brief(...)` block or None when nothing matches.

    Why expansion: the CSV rows are English-only ("Pharmacy/Drug Store",
    "Bakery/Cafe", …). Without `_RU_INDUSTRY_KEYWORDS` mapping, 9/12 typical
    Russian prompts miss palette+font lookups and only get UX-guidelines.

    The 5 UX guidelines we pull are seeded by `project_id` so re-prompts
    within the same project surface the same rules — the model sees rules
    consistently and the brief doesn't churn between turns.

    Charts are gated on `_CHART_SIGNAL_TOKENS` — most landings don't need
    them, and injecting chart guidance noise into a plain marketing site
    is worse than silence.
    """
    if not user_prompt:
        return None
    tokens = _expand_ru_to_en(user_prompt)
    if not tokens:
        return None

    palette = skill_library.lookup_palette(*tokens)
    font_pairing = skill_library.lookup_font_pairing(*tokens)
    # Pass project_id-hashed seed so each project gets a deterministic but
    # different landing-structure pick when keyword matching misses (which
    # is most of the time — landing.csv is structurally indexed, not industry).
    pattern_seed = hash(project_id) if project_id else 0
    landing_pattern = skill_library.lookup_landing_pattern(*tokens, seed=pattern_seed)
    style_preset = skill_library.lookup_style_preset(*tokens)
    icon_family = skill_library.lookup_icon_family(*tokens)

    # Charts only when prompt actually mentions data viz.
    lowered = user_prompt.lower()
    chart_types: tuple = ()
    if any(sig in lowered for sig in _CHART_SIGNAL_TOKENS):
        chart_types = skill_library.lookup_chart_types(*tokens, limit=2)

    # Always emit 5 high-severity UX guidelines so the model has concrete,
    # actionable rules to follow even when the palette/font lookups miss.
    # Seed on project_id so the same project always sees the same rules.
    seed = hash(project_id) if project_id else 0
    guidelines = skill_library.random_ux_guidelines(
        severity="High", limit=5, seed=seed
    )

    brief = skill_library.format_design_brief(
        palette=palette,
        font_pairing=font_pairing,
        landing_pattern=landing_pattern,
        style_preset=style_preset,
        icon_family=icon_family,
        chart_types=chart_types,
        guidelines=guidelines,
    )
    return brief or None


def _format_skill_brief(brief: str) -> str:
    """Wrap the raw `format_design_brief` output in a system-prompt section so
    the model recognises it as authoritative reference rather than a stray
    paragraph. The block is structured + labelled so it survives compression
    and re-summarisation in long-context turns."""
    return (
        "ДИЗАЙН-БРИФ (auto-matched из `ui-ux-pro-max` под этот промпт — "
        "приоритет ВЫШЕ дефолтного `_DESIGN_KIT`. Используй ИМЕННО эти токены "
        "и шрифты; UX-правила обязательны):\n"
        f"{brief}"
    )


def _format_palette_anchor(preset_id: str | None) -> str:
    """Hard top-of-prompt anchor enforcing the preset's palette + fonts.

    Why this exists: the declarative ``format_preset_block`` lives deep in
    the prompt (~midway through ~1200 lines). Haiku 4.5 with a long prompt
    reliably reverts to its training-data default palette (indigo+violet —
    the SaaS aesthetic of Vercel/Linear/shadcn) regardless of what the
    preset block downstream says. Injecting palette+fonts at position #2 —
    right after ``_IDENTITY`` — anchors the model on the correct tokens
    before it reads the rest. Short and imperative so it survives
    long-context compression.

    Returns "" when no preset is selected — caller skips the section.
    """
    if preset_id is None:
        return ""
    preset = PRESETS.get(preset_id)
    if preset is None:
        return ""

    p = preset.palette
    f = preset.fonts
    return f"""\
ОБЯЗАТЕЛЬНАЯ ПАЛИТРА И ШРИФТЫ — anchor, читай ПЕРЕД остальным промптом и держи в голове до конца ответа.

ЦВЕТА — ставь ТОЛЬКО эти HEX, никакие другие в Tailwind config / :root не вводи:
  bg     = {p['bg']}     bg-alt = {p['bg_alt']}
  fg     = {p['fg']}     muted  = {p['muted']}
  accent = {p['accent']}     border = {p['border']}

ШРИФТЫ — подключи ИМЕННО эти Google Fonts, без подмен:
  display: {f['display']}   ·   body: {f['body']}
  <link rel="stylesheet" href="{f['google_fonts_url']}">

КАТЕГОРИЧЕСКИ ЗАПРЕЩЕНЫ (это твои тренировочные дефолты — здесь они БРАК):
  • indigo (#4f46e5 / #6366f1 / #818cf8 / indigo-500/600/700)
  • violet / purple (#7c3aed / #8b5cf6 / #a855f7 / violet-X / purple-X)
  • градиенты типа from-indigo-* to-violet-*, from-purple-* to-pink-*
  • дефолтный SaaS-стек Inter + Space Grotesk если он НЕ в палитре выше

Если палитра выше требует тёмной темы — фон bg, текст fg, accent светлее muted.
Любой цвет/шрифт вне этого блока без явной просьбы пользователя — брак."""


# Templates that ship a visual UI (HTML/JSX). These get the full design
# stack: layout rigor, palette, style preset, visual-richness, image
# generation toggles. The other two container-backed templates (tgbot/api)
# are pure backend and don't get any of that.
_VISUAL_TEMPLATES = {"fullstack", "spa"}
# Templates that are backend-only — Python + asyncio + Postgres. They
# skip every visual block; their `_STACK` text is the entire stack
# guidance.
_BACKEND_TEMPLATES = {"tgbot", "api"}


# ──────────────────────────────────────────────────────────────────────────
# Phase F.2 — Per-tier prompt trimming.
#
# Premium models (Opus/Sonnet/GPT-5) hold focus on a ~14 K-token prompt
# fine. Budget models (Haiku/Nano) routinely drop guidance buried past
# position #5-6 ("lost in the middle"). Balanced (Mini/Yandex/Gigachat)
# sit between — they read the head and tail well but fade in the body.
#
# `_BLOCKS_TO_DROP_BY_TIER` lists which long detail-rich sections we
# remove when assembling for each tier. The dropped blocks are the
# inspirational / detail-polish layers — design floor (palette anchor,
# AWWWARDS_PRINCIPLES, _LAYOUT_RIGOR, _FUNCTIONAL_CONTRACT, _SELF_CHECK,
# _RESPONSE, _STACK) stays for every tier.
#
# Budget tier ALSO routes through `multipass_generate` (Phase B) when
# `MULTIPASS_MODELS` env-flag is set — this trim path is the fallback
# single-shot for the same models when multipass is disabled.
# ──────────────────────────────────────────────────────────────────────────


def _drop_blocks_for_tier(sections: tuple[str, ...], tier: str) -> tuple[str, ...]:
    """Return `sections` with the per-tier heavy blocks filtered out.

    Filtering uses identity-equality (`is`) because the dropped sentinels
    are module-level string constants — every `sections` tuple holds the
    same `_DETAILS_KIT` / `_SIGNATURE_MOVES` / … object, never a copy.
    """
    if tier == "premium":
        return sections
    drop: set[str]
    if tier == "budget":
        # Aggressive trim — cuts ~6 KB out of the ~14 KB prompt so Haiku
        # 4.5 keeps focus through the head→tail span. The blocks dropped
        # are the depth-of-taste polish layers; the design floor stays.
        drop = {_DETAILS_KIT, _SIGNATURE_MOVES, _TASTE, _STYLE_KIT, _VISUAL_RICH_KIT}
    elif tier == "balanced":
        # Light trim — keeps the visual richness + style kit, drops only
        # the deeply prose-y inspirational layers that overflow attention
        # on mid-tier models.
        drop = {_DETAILS_KIT, _SIGNATURE_MOVES, _TASTE}
    else:
        return sections
    return tuple(s for s in sections if s not in drop)


def build_system_prompt(
    template: str,
    preset_id: str | None = None,
    image_gen_enabled: bool = True,
    *,
    skill_brief: str | None = None,
    model_id: str | None = None,
) -> str:
    """Собрать system prompt под тип проекта.

    Шесть веток:
    * ``fullstack`` (Next.js + Postgres + Drizzle + Auth.js) — _NEXT_STACK
    * ``spa`` (Vite + React + react-router) — _SPA_STACK
    * ``tgbot`` (aiogram 3 + asyncpg) — _TGBOT_STACK, бэкенд only
    * ``api`` (FastAPI + SQLAlchemy 2 + JWT) — _API_STACK, бэкенд only
    * ``blank/landing/portfolio/blog`` — _STATIC_STACK + _ANIMATION_KIT

    `_VISUAL_TEMPLATES` (fullstack + spa) получают полный набор визуальных
    блоков — layout rigor, design kit, style kit (только static), visual
    richness, image-toggle. `_BACKEND_TEMPLATES` (tgbot + api) пропускают
    их полностью — там нет HTML, нет картинок, нет layout'а.

    ``preset_id`` (Awwwards-tier пресет из ``design_presets.PRESETS``) — опциональный.
    Если указан, declarative-блок пресета инжектится СРАЗУ после ``_STYLE_KIT`` и
    overrides default «Refined Minimal». ``AWWWARDS_PRINCIPLES`` (8 сквозных
    floor-правил) инжектится ВСЕГДА — даже без preset_id. Только для визуальных
    шаблонов; бэкенд их игнорирует.

    ``image_gen_enabled`` — какой из image-блоков инжектится. Только для визуальных.

    ``skill_brief`` (keyword-only) — design brief from ui-ux-pro-max. Только
    для визуальных.
    """
    preset_block = format_preset_block(preset_id) if preset_id else ""
    palette_anchor = _format_palette_anchor(preset_id)
    skill_block = _format_skill_brief(skill_brief) if skill_brief else ""
    image_block = _IMAGE_GEN_ON if image_gen_enabled else _IMAGE_GEN_OFF

    # Phase A.1 — design_anchor unifies palette_anchor + skill_brief at
    # position #2 of the prompt. Long context (~14K) creates a "lost in
    # the middle" problem where Haiku 4.5 routinely ignores guidance
    # buried after position #5-6. Anchoring BOTH at the top forces the
    # model to read them before _QUALITY_BAR.
    design_anchor_parts = [p for p in (palette_anchor, skill_block) if p]
    design_anchor = "\n\n".join(design_anchor_parts) if design_anchor_parts else ""

    # Phase A.2 — _DESIGN_KIT is the generic catalog of per-industry
    # palettes. When the project ALREADY has a preset palette and/or a
    # matched skill brief (both more specific than the catalog), the
    # catalog becomes a third competing source of truth — the model
    # randomly picks between them. Suppress the catalog when we have
    # anchored guidance; keep it as the fallback when we have nothing.
    include_design_kit = (preset_id is None) and (not skill_block)

    if template == "fullstack":
        sections: tuple[str, ...] = (
            _IDENTITY,
            *((design_anchor,) if design_anchor else ()),
            _KIT_V3_REFERENCE,
            _QUALITY_BAR,
            _LAYOUT_RIGOR,
            AWWWARDS_PRINCIPLES,
            *((_DESIGN_KIT,) if include_design_kit else ()),
            *((preset_block,) if preset_block else ()),
            _VISUAL_RICH_KIT,
            image_block,
            _FUNCTIONAL_CONTRACT,
            _FULLSTACK_STACK,
            _SELF_CHECK,
            _RESPONSE,
        )
    elif template == "spa":
        sections = (
            _IDENTITY,
            *((design_anchor,) if design_anchor else ()),
            _KIT_V3_REFERENCE,
            _QUALITY_BAR,
            _LAYOUT_RIGOR,
            AWWWARDS_PRINCIPLES,
            *((_DESIGN_KIT,) if include_design_kit else ()),
            *((preset_block,) if preset_block else ()),
            _VISUAL_RICH_KIT,
            image_block,
            _FUNCTIONAL_CONTRACT,
            _SPA_STACK,
            _SELF_CHECK,
            _RESPONSE,
        )
    elif template == "tgbot":
        # Bot is text-only — no design kit, no layout rigor, no image
        # generation. Just identity + Russian quality bar + stack rules.
        sections = (
            _IDENTITY,
            _QUALITY_BAR,
            _TGBOT_STACK,
            _RESPONSE,
        )
    elif template == "api":
        # API is JSON-only — no UI concerns. Same minimal shape as tgbot.
        sections = (
            _IDENTITY,
            _QUALITY_BAR,
            _API_STACK,
            _RESPONSE,
        )
    else:
        # Static V1: blank / landing / portfolio / blog
        sections = (
            _IDENTITY,
            *((design_anchor,) if design_anchor else ()),
            _KIT_V3_REFERENCE,
            _QUALITY_BAR,
            _LAYOUT_RIGOR,
            AWWWARDS_PRINCIPLES,
            _TASTE,
            *((_DESIGN_KIT,) if include_design_kit else ()),
            _STYLE_KIT,
            *((preset_block,) if preset_block else ()),
            _DETAILS_KIT,
            _SIGNATURE_MOVES,
            _VISUAL_RICH_KIT,
            image_block,
            _FUNCTIONAL_CONTRACT,
            _STATIC_STACK,
            _ANIMATION_KIT,
            _SELF_CHECK,
            _RESPONSE,
        )

    # Phase F.2 — apply per-tier trim. Budget/balanced models drop the
    # detail-polish layers so the head→tail span fits their attention
    # budget. Premium-tier and missing-model_id calls keep the full
    # prompt (the missing-id path covers internal callers like the
    # preset classifier that don't speak in tier terms).
    if model_id is not None:
        sections = _drop_blocks_for_tier(sections, tier_for_model(model_id))
    return "\n\n".join(sections)


def _format_selection_block(selected_elements: Sequence[dict[str, Any]]) -> str:
    """Собрать блок «пользователь выделил элементы в превью» для промпта.

    Когда юзер тыкает в элементы превью (select-mode) и пишет к каждому
    комментарий, мы вкладываем это в финальное user-сообщение. Инструкция —
    инлайн и на языке домена (R-08): «выделенный элемент» = адресная правка,
    модель меняет ТОЛЬКО эти блоки, остальную страницу сохраняет. `outerHTML`
    и видимый текст помогают модели найти элемент в исходнике (она правит код,
    а не DOM), поэтому важнее селектора.
    """
    lines = [
        "[Контекст правки: пользователь выделил элементы прямо в превью и оставил "
        "к каждому комментарий. Это ТОЧЕЧНАЯ правка — измени только эти элементы "
        "(и то, что просят с ними сделать). Остальную страницу — вёрстку, тексты, "
        "стили, не относящиеся к выделению — сохрани без изменений.]",
        "",
        "Выделенные элементы:",
    ]
    for i, el in enumerate(selected_elements, 1):
        selector = (el.get("selector") or "").strip()
        label = (el.get("label") or "").strip()
        text = (el.get("text") or "").strip()
        html = (el.get("html") or "").strip()
        comment = (el.get("comment") or "").strip()
        head = f"{i}. Селектор: {selector}"
        if label:
            head += f"  ·  {label}"
        lines.append(head)
        if text:
            lines.append(f"   Видимый текст: {text}")
        if html:
            lines.append(f"   HTML: {html}")
        lines.append(f"   Что сделать: {comment or '(см. сообщение ниже)'}")
    return "\n".join(lines)


def build_messages(
    current_files: dict[str, str],
    history: Sequence[dict[str, str]],
    user_prompt: str,
    template: str = "blank",
    selected_elements: Sequence[dict[str, Any]] | None = None,
    preset_id: str | None = None,
    image_gen_enabled: bool = True,
    project_id: str | None = None,
    model_id: str | None = None,
) -> list[dict[str, str]]:
    # Pull a `ui-ux-pro-max` design brief for this specific prompt — palette
    # + font pairing + 5 high-severity UX rules. Skipped silently when the
    # prompt has no industry-token signal we can match against
    # (`_compute_skill_brief` returns None).
    skill_brief = _compute_skill_brief(user_prompt, project_id)

    # Phase F.2 — `model_id` flows into `build_system_prompt` so the prompt
    # assembler can trim heavy blocks for budget/balanced tiers. Omitted
    # by default = premium-tier full prompt (backward-compatible).
    messages: list[dict[str, str]] = [
        {
            "role": "system",
            "content": build_system_prompt(
                template,
                preset_id,
                image_gen_enabled,
                skill_brief=skill_brief,
                model_id=model_id,
            ),
        }
    ]

    if current_files:
        files_block = "\n\n".join(
            f'<file path="{path}">\n{content}\n</file>'
            for path, content in current_files.items()
        )
        messages.append(
            {
                "role": "user",
                "content": f"Текущее состояние проекта:\n{files_block}",
            }
        )

    for m in list(history)[-HISTORY_LIMIT:]:
        if m.get("role") in {"user", "assistant"} and m.get("content"):
            messages.append({"role": m["role"], "content": m["content"]})

    final_user = user_prompt
    if selected_elements:
        final_user = _format_selection_block(selected_elements) + "\n\n" + user_prompt
    messages.append({"role": "user", "content": final_user})
    return messages
