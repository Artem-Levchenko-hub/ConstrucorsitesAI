def build_system_prompt(
    template: str,
    preset_id: str | None = None,
    image_gen_enabled: bool = True,
    *,
    skill_brief: str | None = None,
    model_id: str | None = None,
    design_tokens_block: str | None = None,
) -> str:
    """Собрать system prompt под тип проекта.

    Семь веток:
    * ``fullstack`` (Next.js + Postgres + Drizzle + Auth.js) — _FULLSTACK_STACK
    * ``nextjs_entities`` (Base44-style entity engine + generative frontend) — _ENTITIES_STACK
    * ``spa`` (Vite + React + react-router) — _SPA_STACK
    * ``tgbot`` (aiogram 3 + asyncpg) — _TGBOT_STACK, бэкенд only
    * ``api`` (FastAPI + SQLAlchemy 2 + JWT) — _API_STACK, бэкенд only
    * ``blank/landing/portfolio/blog`` — _STATIC_STACK + _ANIMATION_KIT

    `_VISUAL_TEMPLATES` (fullstack + spa) получают полный набор визуальных
    блоков — layout rigor, design kit, style kit (только static), visual
    richness, image-toggle. `_BACKEND_TEMPLATES` (tgbot + api) пропускают
    их полностью — там нет HTML, нет картинок, нет layout'а.

    ``preset_id`` (Awwwards-tier пресет из ``design_presets.PRESETS``) — опциональный.
    Если указан, declarative-блок пресета инжектится СРАЗУ после ``_STYLE_KIT`` и
    overrides default «Refined Minimal». ``AWWWARDS_PRINCIPLES`` (8 сквозных
    floor-правил) инжектится ВСЕГДА — даже без preset_id. Только для визуальных
    шаблонов; бэкенд их игнорирует.

    ``image_gen_enabled`` — какой из image-блоков инжектится. Только для визуальных.

    ``skill_brief`` (keyword-only) — design brief from ui-ux-pro-max. Только
    для визуальных.
    """
    # Phase 11 — seeded design tokens (freeform mode) are the authoritative
    # palette+font source and SUPPRESS the competing fixed-preset blocks
    # (preset palette, palette anchor) so the model can't drift between three
    # palettes. Without tokens, behaviour is byte-for-byte unchanged.
