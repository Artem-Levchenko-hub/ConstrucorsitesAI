# A prior BUILD answer saved in chat history is the page's full <file>…</file>
# HTML (30–60 KB). In EDIT mode the CURRENT file is already supplied verbatim as
# «текущее состояние», so that stale copy in history is pure waste: it ~doubles
# the input the gateway bills AND tempts the cheap model to copy a byte-exact
# SEARCH from the OUTDATED markup → a silent conflict + a wasted retry. Replace
# those blocks with a short marker; keep the rest (small <edit> diffs carry the
# user's recent intent). Edit-only — the build path keeps its history verbatim.
_FILE_BLOCK_RE = re.compile(r"<file\b[^>]*>.*?</file>", re.S | re.I)


def _history_for_edit(history: Sequence[dict[str, str]]) -> list[dict[str, str]]:
    """Last ``HISTORY_LIMIT`` user/assistant turns with full-file HTML stripped."""
    out: list[dict[str, str]] = []
    for m in list(history)[-HISTORY_LIMIT:]:
        if m.get("role") in {"user", "assistant"} and m.get("content"):
            content = _FILE_BLOCK_RE.sub("[предыдущая сборка страницы]", m["content"])
            out.append({"role": m["role"], "content": content})
    return out


def _build_edit_messages(
    current_files: dict[str, str],
    history: Sequence[dict[str, str]],
    user_prompt: str,
    selected_elements: Sequence[dict[str, Any]] | None,
    template: str = "blank",
) -> list[dict[str, str]]:
    """Lean message list for a SURGICAL EDIT (CHEAP intent).

    System prompt = preserve-everything identity + kit-reuse hint + <edit>-only
    response format. The current file state is fed verbatim (the model needs it
    to write byte-exact SEARCH blocks), framed as "patch this, don't rewrite".
    No build kit, no palette anchor, no self-check — so the cheap model can't be
    pushed into regenerating the page or re-rolling the palette.
    """
    # Situational hints are GATED by request keywords. The cheap edit model copies
    # whatever recipe sits in context, so a dark-theme bg example or an image-gen
    # recipe present on EVERY edit derails unrelated text edits (the measured bug:
    # "смени заголовок" → the page went dark because _EDIT_BG_HINT's «Тихая роскошь
    # = тёмный фон + золото» example was always in scope). Include a hint ONLY when
    # the request is actually about that dimension.
    _p = (user_prompt or "").lower()
    _wants_bg = any(
        k in _p for k in (
            "фон", "background", "цвет", "палитр", "градиент",
            "тёмн", "темн", "светл", "оттенок", " тон",
        )
    )
    _wants_img = any(
        k in _p for k in (
            "картин", "фото", "изображ", "image", "баннер",
            "иллюстрац", "генери", "сгенер", "снимок",
        )
    )
    _wants_add = any(
        k in _p for k in ("добав", "вставь", "встав", "ещё", "еще", "новый блок", "секци")
    )
    # Stack-aware: container stacks (Next.js/React/entities) have NO index.html —
    # pages are src/app/**/page.tsx. The static-HTML edit prompt (index.html,
    # DOCTYPE, omnia-kit) makes the model emit <edit path="index.html"> which can
    # never land on a .tsx project → the user sees "правка не применилась". Give
    # those stacks a React-aware identity/response instead.
    if template in ("fullstack", "nextjs_entities"):
        _blocks: list[str] = [_EDIT_IDENTITY_NEXT, _EDIT_FAITHFUL, _EDIT_RESPONSE_NEXT]
    else:
        _blocks = [_EDIT_IDENTITY, _EDIT_FAITHFUL]
        if _wants_add:
            _blocks.append(_EDIT_KIT_HINT)
        if _wants_bg:
            _blocks.append(_EDIT_BG_HINT)
        if _wants_img:
            _blocks.append(_IMAGE_GEN_HINT)
        _blocks.append(_EDIT_RESPONSE)
    system = "\n\n".join(_blocks)
    messages: list[dict[str, str]] = [{"role": "system", "content": system}]

    if current_files:
        files_block = "\n\n".join(
            f'<file path="{path}">\n{content}\n</file>'
            for path, content in current_files.items()
        )
        messages.append(
            {
                "role": "user",
                "content": (
                    "Текущее состояние страницы — правь его ТОЧЕЧНО через <edit>, "
                    "не переписывай целиком:\n" + files_block
                ),
            }
        )

    messages.extend(_history_for_edit(history))

    final_user = user_prompt
    if selected_elements:
        final_user = _format_selection_block(selected_elements) + "\n\n" + user_prompt
    messages.append({"role": "user", "content": final_user})
    return messages


