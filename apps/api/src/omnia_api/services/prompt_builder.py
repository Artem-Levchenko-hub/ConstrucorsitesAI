def build_system_prompt(
    template: str,
    preset_id: str | None = None,
    image_gen_enabled: bool = True,
) -> str:
    """Собрать system prompt под тип проекта. `fullstack` → Next.js, иначе статика.

    Дизайн-кит и контракт «ноль тупиков» применяются в обоих режимах. Визуальные
    пресеты (`_STYLE_KIT`) и встроенный кит анимаций (`_ANIMATION_KIT`) — только для
    статики: в fullstack-режиме файлов кита нет. Self-check идёт последним.

    ``preset_id`` (Awwwards-tier пресет из ``design_presets.PRESETS``) — опциональный.
    Если указан, declarative-блок пресета инжектится СРАЗУ после ``_STYLE_KIT`` и
    overrides default «Refined Minimal». ``AWWWARDS_PRINCIPLES`` (8 сквозных
    floor-правил) инжектится ВСЕГДА — даже без preset_id.

    ``image_gen_enabled`` управляет тем, какой из image-блоков инжектится:
    `_IMAGE_GEN_ON` (со спец-тегом `data-omnia-gen`) или `_IMAGE_GEN_OFF`
    (только CSS/SVG, тег запрещён). `_VISUAL_RICH_KIT` инжектится ВСЕГДА —
    визуальная насыщенность обязательна и не зависит от toggle.
    """
    preset_block = format_preset_block(preset_id) if preset_id else ""
    image_block = _IMAGE_GEN_ON if image_gen_enabled else _IMAGE_GEN_OFF

    if template == "fullstack":
        sections: tuple[str, ...] = (
            _IDENTITY,
            _QUALITY_BAR,
            AWWWARDS_PRINCIPLES,
            _DESIGN_KIT,
            *((preset_block,) if preset_block else ()),
            _VISUAL_RICH_KIT,
            image_block,
            _FUNCTIONAL_CONTRACT,
            _FULLSTACK_STACK,
            _SELF_CHECK,
            _RESPONSE,
        )
    else:
        sections = (
            _IDENTITY,
            _QUALITY_BAR,
            AWWWARDS_PRINCIPLES,
            _TASTE,
            _DESIGN_KIT,
            _STYLE_KIT,
            *((preset_block,) if preset_block else ()),
            _DETAILS_KIT,
            _SIGNATURE_MOVES,
            _VISUAL_RICH_KIT,
            image_block,
            _FUNCTIONAL_CONTRACT,
            _STATIC_STACK,
            _ANIMATION_KIT,
            _SELF_CHECK,
            _RESPONSE,
        )
    return "\n\n".join(sections)


def _format_selection_block(selected_elements: Sequence[dict[str, Any]]) -> str:
    """Собрать блок «пользователь выделил элементы в превью» для промпта.

    Когда юзер тыкает в элементы превью (select-mode) и пишет к каждому
    комментарий, мы вкладываем это в финальное user-сообщение. Инструкция —
    инлайн и на языке домена (R-08): «выделенный элемент» = адресная правка,
    модель меняет ТОЛЬКО эти блоки, остальную страницу сохраняет. `outerHTML`
    и видимый текст помогают модели найти элемент в исходнике (она правит код,
    а не DOM), поэтому важнее селектора.
    """
    lines = [
        "[Контекст правки: пользователь выделил элементы прямо в превью и оставил "
        "к каждому комментарий. Это ТОЧЕЧНАЯ правка — измени только эти элементы "
        "(и то, что просят с ними сделать). Остальную страницу — вёрстку, тексты, "
        "стили, не относящиеся к выделению — сохрани без изменений.]",
        "",
        "Выделенные элементы:",
    ]
    for i, el in enumerate(selected_elements, 1):
        selector = (el.get("selector") or "").strip()
        label = (el.get("label") or "").strip()
        text = (el.get("text") or "").strip()
        html = (el.get("html") or "").strip()
        comment = (el.get("comment") or "").strip()
        head = f"{i}. Селектор: {selector}"
        if label:
            head += f"  ·  {label}"
        lines.append(head)
        if text:
            lines.append(f"   Видимый текст: {text}")
        if html:
            lines.append(f"   HTML: {html}")
        lines.append(f"   Что сделать: {comment or '(см. сообщение ниже)'}")
    return "\n".join(lines)


def build_messages(
    current_files: dict[str, str],
    history: Sequence[dict[str, str]],
    user_prompt: str,
    template: str = "blank",
    selected_elements: Sequence[dict[str, Any]] | None = None,
    preset_id: str | None = None,
    image_gen_enabled: bool = True,
) -> list[dict[str, str]]:
    messages: list[dict[str, str]] = [
        {
            "role": "system",
            "content": build_system_prompt(template, preset_id, image_gen_enabled),
        }
    ]

    if current_files:
        files_block = "\n\n".join(
            f'<file path="{path}">\n{content}\n</file>'
            for path, content in current_files.items()
        )
        messages.append(
            {
                "role": "user",
                "content": f"Текущее состояние проекта:\n{files_block}",
            }
        )

    for m in list(history)[-HISTORY_LIMIT:]:
        if m.get("role") in {"user", "assistant"} and m.get("content"):
            messages.append({"role": m["role"], "content": m["content"]})

    final_user = user_prompt
    if selected_elements:
        final_user = _format_selection_block(selected_elements) + "\n\n" + user_prompt
    messages.append({"role": "user", "content": final_user})
    return messages
