HISTORY_LIMIT = 6


def build_system_prompt(template: str) -> str:
    """Собрать system prompt под тип проекта. `fullstack` → Next.js, иначе статика.

    Дизайн-кит и контракт «ноль тупиков» применяются в обоих режимах; self-check
    идёт последним, чтобы модель проверила готовый результат перед выдачей.
    """
    stack = _FULLSTACK_STACK if template == "fullstack" else _STATIC_STACK
    return "\n\n".join(
        (
            _IDENTITY,
            _QUALITY_BAR,
            _DESIGN_KIT,
            _FUNCTIONAL_CONTRACT,
            stack,
            _SELF_CHECK,
            _RESPONSE,
        )
    )


def build_messages(
    current_files: dict[str, str],
    history: Sequence[dict[str, str]],
    user_prompt: str,
    template: str = "blank",
) -> list[dict[str, str]]:
    messages: list[dict[str, str]] = [
        {"role": "system", "content": build_system_prompt(template)}
    ]

    if current_files:
        files_block = "\n\n".join(
            f'<file path="{path}">\n{content}\n</file>'
            for path, content in current_files.items()
        )
        messages.append(
            {
                "role": "user",
                "content": f"Текущее состояние проекта:\n{files_block}",
            }
        )

    for m in list(history)[-HISTORY_LIMIT:]:
        if m.get("role") in {"user", "assistant"} and m.get("content"):
            messages.append({"role": m["role"], "content": m["content"]})

    messages.append({"role": "user", "content": user_prompt})
    return messages
